    /**
     * @return mixed
     * Used to send email after joining an event
     */
    public function sendEmail($user,$event)
    {
        $fname = explode(" ", $user->name);

        $data = array( 'email' => $user->email, 'name' => $fname[0]);

        Mail::send('emails.joined', array('user' => $user , 'event' => $event), function($message) use ($data)
        {
            $message->to($data['email'], $data['name'])->subject('Operation Love - RCM');
        });
        return ;
    }

